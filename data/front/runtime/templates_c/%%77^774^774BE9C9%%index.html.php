        //获取开启外包之旅的位置绝对高度
        var $scroll = getElementTop($(".scroll").get(0));

        if($scrollTop>=$(".head").height()){
            $(".head").addClass('fixed');
            if($scrollTop>=$change){
                $(".head").addClass('bgWhite');
                $(".logo_img").attr("src","__PUBLIC__/images/logo_black.png");
                $(".naw_img").attr("src","__PUBLIC__/images/naw_black.png");
            }else{
                $(".head").removeClass('bgWhite');
                $(".logo_img").attr("src","__PUBLIC__/images/logo.png");
                $(".naw_img").attr("src","__PUBLIC__/images/naw.png");
            }
        }else{
            $(".head").removeClass("fixed bgWhite");
        }
        //画轴展开

        if($scrollTop+200>=$scroll&&flag){
            $(".scroll-main").addClass('scroll-active');
            $(".scroll-main:after").css("animation-play-state","play");
            $(".scroll-main").one("animationend",function () {
                $(".scroll-btn").fadeIn(300);
                $(".scroll-main").removeClass('scroll-active');
                flag=false;
            });
            $(".scroll-main").one("webkitAnimationEnd",function () {
                $(".scroll-btn").fadeIn(300);
                $(".scroll-main").removeClass('scroll-active');
                flag=false;
            });

        }

    })




