            　　　　　　}
        　　　　}
    　　}

    function getElementTop(obj) {
        var actualTop=obj.offsetTop;
        var current=obj.offsetParent;
        while(current!==null){
            actualTop+=current.offsetTop;
            current=current.offsetParent;
        }
        return actualTop;
    }

